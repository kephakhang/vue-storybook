/**
 * @description
 * timeScale에 따라 dateFormat을 다르게 설정합니다.
 * 기간 및 timeScale을 보여주는 x축 내용을 설정할 때 사용합니다.
 * @param {string} time - 'YYYYMMDDhhmm' 형식의 날짜 문자열.
 * @param {string} timeScale - ("MINUTELY", "HOURLY", "DAILY", "WEEKLY", "MONTHLY", "YEARLY")
 */
function getDataTitle(time, timeScale) {
  let dateFormat = "";

  if (timeScale === "MINUTELY") {
    dateFormat = "YYYY-MM-DD HH:mm";
  }
  if (timeScale === "HOURLY") {
    dateFormat = "YYYY-MM-DD HH [(H)]";
  }
  if (timeScale === "DAILY") {
    dateFormat = "YYYY-MM-DD";
  }
  if (timeScale === "WEEKLY") {
    dateFormat = "YYYY-WW [(W)] [(] YYYY-MM-DD [)]";
  }
  if (timeScale === "MONTHLY") {
    dateFormat = "YYYY-MM [(M)]";
  }
  if (timeScale === "YEARLY") {
    dateFormat = "YYYY";
  }
  return moment(time, "YYYYMMDDhhmm").format(dateFormat);
}

